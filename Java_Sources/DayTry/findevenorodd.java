package DayTry;

public class findevenorodd {
    public static void main(String[] args){
        int num = 16;
        if(num%2 ==0){
            System.out.println("Given number " + num + " is Even number.");
        }
        else {
            System.out.println("Given number " + num + " is Odd number.");
        }
    }
}
